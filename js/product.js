// Session storage
function details(arrIndex){
     sessionStorage.detail=JSON.stringify(arrItems[arrIndex]); // Covert the new obj to str then store to session storage
     window.location.href="item.html"; // Link to Item page
}

// Local storage
function add_Product(arrIndex){
    alert(arrItems[arrIndex].type+" added to cart!");

    var arrItem;
    if(typeof(Storage)!=="undefined"){

        if(localStorage.item){
            arrItem=JSON.parse(localStorage.item); // Convert the array inside the local storage, str to obj
        }else{
            arrItem=[];
        }

        arrItem.push(arrItems[arrIndex]); // add the new item to the array
        localStorage.item = JSON.stringify(arrItem); // Covert the array back to str then store to local storage

    }
    else{
        alert("Storage API is not supported by this browser.");
    }
}

